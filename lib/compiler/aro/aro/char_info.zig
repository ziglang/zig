/// C11 standard Annex D
pub fn isC11DisallowedInitialIdChar(codepoint: u21) bool {
    assert(codepoint > 0x7F);
    return switch (codepoint) {
        0x0300...0x036F,
        0x1DC0...0x1DFF,
        0x20D0...0x20FF,
        0xFE20...0xFE2F,
        => true,
        else => false,
    };
}

